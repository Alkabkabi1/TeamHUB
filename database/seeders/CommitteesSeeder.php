    /**
     * Seed the CS club's committees so all three committee screens render with
     * real data (listing, page, dashboard).
     */
    public function run(): void
    {
        $csClub = Club::query()->where('name', 'نادي الحاسبات')->first();

        if ($csClub === null) {
            return;
        }

        foreach ($this->committees as $data) {
            $committee = Committee::query()->updateOrCreate(
                ['club_id' => $csClub->id, 'name' => $data['name']],
                ['description' => $data['description'], 'status' => 'active'],
            );

            $lead = isset($data['lead'])
                ? User::query()->where('email', $data['lead'])->first()
                : null;
            if ($lead !== null) {
                $this->seedMembership($lead, $committee, [CommitteeRole::CommitteeLead]);
            }

            $member = isset($data['member'])
                ? User::query()->where('email', $data['member'])->first()
                : null;
            if ($member !== null) {
                $this->seedMembership($member, $committee, [CommitteeRole::Member]);
            }

            $this->seedContent($committee, $csClub->id);
        }
    }

    /**
     * @param  array<int, CommitteeRole>  $roles
     */
    private function seedMembership(User $user, Committee $committee, array $roles): void
    {
        $membership = CommitteeMembership::query()->firstOrCreate(
            ['user_id' => $user->id, 'committee_id' => $committee->id],
            [
                'status' => 'approved',
                'requested_at' => now()->subMonths(6),
                'reviewed_by' => $user->id,
                'reviewed_at' => now()->subMonths(6),
                'joined_at' => now()->subMonths(6),
            ],
        );

        $membership->assignCommitteeRole(CommitteeRole::Member);

        foreach ($roles as $role) {
            $membership->assignCommitteeRole($role);
        }
    }

    private function seedContent(Committee $committee, int $clubId): void
    {
        if ($committee->posts()->doesntExist()) {
            Post::factory()->count(2)->create([
                'club_id' => $clubId,
                'committee_id' => $committee->id,
                'published_at' => now()->subDays(fake()->numberBetween(1, 30)),
            ]);
        }
    }
