    /**
     * @return array<string, mixed>
     */
    public function definition(): array
    {
        $startsAt = fake()->dateTimeBetween('+1 day', '+30 days');
        $endsAt = (clone $startsAt)->modify('+2 hours');

        return [
            'club_id' => Club::factory(),
            'title' => fake()->randomElement(self::TITLES),
            'description' => fake()->randomElement(self::DESCRIPTIONS),
            'starts_at' => $startsAt,
            'ends_at' => $endsAt,
            'location' => fake()->randomElement(self::LOCATIONS),
            'capacity' => fake()->numberBetween(20, 200),
            'status' => 'active',
        ];
    }

    public function upcoming(): static
    {
        return $this->state(function () {
            $startsAt = fake()->dateTimeBetween('+1 day', '+30 days');

            return [
                'starts_at' => $startsAt,
                'ends_at' => (clone $startsAt)->modify('+2 hours'),
            ];
        });
    }

    public function past(): static
    {
        return $this->state(function () {
            $startsAt = fake()->dateTimeBetween('-30 days', '-1 day');

            return [
                'starts_at' => $startsAt,
                'ends_at' => (clone $startsAt)->modify('+2 hours'),
            ];
        });
    }

    /**
     * Attach the given number of generated cover images to the created event.
     * Each image is tinted with the club's theme color, so an event's gallery
     * visually belongs to its club.
     */
    public function withImages(int $count = 1): static
    {
        return $this->afterCreating(function (Event $event) use ($count): void {
            for ($i = 0; $i < $count; $i++) {
                $bytes = DemoCoverImage::generate("event-{$event->id}-{$i}", $event->club?->theme);

                $event->addMediaFromString($bytes)
                    ->usingFileName("cover-{$i}.jpg")
                    ->toMediaCollection(Event::IMAGE_COLLECTION);
            }
        });
    }
